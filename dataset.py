import torch
import tiktoken
import numpy as np

class DataLoaderTiny:
    name="tinyshakespeare"
    def __init__(self, B, T, process_rank, num_processes, split=None,print_log=True ):
        self.B = B
        self.T = T
        self.process_rank = process_rank
        self.num_processes = num_processes
        self.split = split

        # at init load tokens from disk and store them in memory
        with open('input.txt', 'r') as f:
            text = f.read()
        enc = tiktoken.get_encoding('gpt2')

        assert split in ["train", "val", None]
        split_idx = int(0.8*len(text)) # 80-20 Split
        if split == "train":
            text = text[:split_idx]
        elif split == "val":
            text = text[split_idx:]

        tokens = enc.encode(text)
        self.tokens = torch.tensor(tokens)
        if print_log:
            print(f"Dataset : {self.name} Split:{split} - loaded {len(self.tokens)} tokens")

        self.reset()

    def reset(self):
        # state
        self.current_position = self.B * self.T * self.process_rank


    def next_batch(self):
        B, T = self.B, self.T
        buf = self.tokens[self.current_position : self.current_position+B*T+1]
        x = (buf[:-1]).view(B, T) # inputs
        y = (buf[1:]).view(B, T) # targets
        # advance the position in the tensor
        self.current_position += B * T * self.num_processes
        # if loading the next batch would be out of bounds, reset
        if self.current_position + (B * T * self.num_processes + 1) > len(self.tokens):
            self.current_position = self.B * self.T * self.process_rank
        return x, y
    


def load_tokens(filename):
    npt = np.load(filename)
    npt = npt.astype(np.int32) # added after video
    ptt = torch.tensor(npt, dtype=torch.long)
    return ptt

